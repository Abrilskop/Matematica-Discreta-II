# AND
# tenemos un bucle que repetira su proceso 2 veces en una a=true
for A in [True, False]:
 for B in [True, False]:
  print(A," AND ", B," = ", A and B)

# OR
for A in [True, False]:
 for B in [True, False]:
  print(A," OR ", B," = ", A or B)

# NOT
# tenemos un bucle que repetira su proceso 2 veces en una a=true
for A in [True, False]:
 for B in [True, False]:
  print(A," NOT ", B," = ", not B)